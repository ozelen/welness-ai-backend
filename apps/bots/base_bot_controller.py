from abc import ABC, abstractmethod

# Base class for all bot controllers
class BaseBotController(ABC):
    @abstractmethod
    def start(self):
        pass

    @abstractmethod
    def stop(self):
        pass

    @abstractmethod
    def run(self):
        pass

    @abstractmethod
    def run(self):
        pass

    @abstractmethod
    def __str__(self):
        pass

